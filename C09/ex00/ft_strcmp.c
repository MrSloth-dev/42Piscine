int	ft_scmp(char *s1, char *s2)
{
	int	diff;
	int	i;
	
	diff = 0;
	i = 0;
	while ((s1[i] || s2[i]) && diff == 0)
	{
		diff = s1[i] - s2[i];
		i++;
	}
	return (diff);
}
